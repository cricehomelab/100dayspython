# Examples of String Concatonation.
# there will not be a space in this example
print("Hello" + "world!")
# each example here will output "Hello world!" with a space between "hello" and "world!"
# these are just 3 different ways to do the same thing.
print("Hello " + "world!")
print("Hello" + " world!")
print("Hello" + " " + "world!")
